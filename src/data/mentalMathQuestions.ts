// Helper function to get questions by level
export const getQuestionsByLevel = (level: number): MentalMathQuestion[] => {
  return mentalMathQuestions.filter(q => q.level === level);
};

// Helper function to get random questions for a level
export const getRandomQuestionsForLevel = (level: number, count: number): MentalMathQuestion[] => {
  const levelQuestions = getQuestionsByLevel(level);
  const shuffled = [...levelQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};
